#include "libft.h"

int			ft_sqrt(int nb)
{
	int		res;
	int		i;

	i = 1;
	res = 0;
	while (i <= nb / 2)
	{
		res = i * i;
		if (res == nb)
			return (i);
		i++;
	}
	return (0);
}
