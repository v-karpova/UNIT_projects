#include "libft.h"

void	ft_putnbr(int n)
{
	if (n == -2147483648)
	{
		n = n % 1000000000;
		n = -n;
		ft_putchar('-');
		ft_putchar('2');
	}
	if (n < 0 && n != -2147483648)
	{
		n = -n;
		ft_putchar('-');
	}
	if (n < 10)
		ft_putchar(n + '0');
	else
	{
		ft_putnbr(n / 10);
		ft_putchar(n % 10 + '0');
	}
}
